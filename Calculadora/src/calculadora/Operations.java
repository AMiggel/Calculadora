
package calculadora;


public class Operations {
    
    public double suma(double numero_1,double numero_2){
        return numero_1 + numero_2;
    }
    public double dividir(double numero_1,double numero_2){
        return numero_1 / numero_2;
    }
    public double resta(double numero_1,double numero_2){
        return numero_1 - numero_2;
    }
    public double multiplicar(double numero_1,double numero_2){
        return numero_1 - numero_2;
    }
    
}
